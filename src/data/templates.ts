// Helper functions
export const getUniqueThemes = (): string[] => {
  const allThemes = templates.flatMap(t => t.themes);
  return [...new Set(allThemes)];
};

export const getUniqueFormats = (): string[] => {
  const allFormats = templates.flatMap(t => t.formats);
  return [...new Set(allFormats)];
};

export const getUniqueObjectives = (): string[] => {
  const allObjectives = templates.flatMap(t => t.objectives);
  return [...new Set(allObjectives)];
};

export const getTrendingTemplates = (): Template[] => {
  return templates.filter(t => t.isTrending);
};

